package yong.map.model;

public class MapDTO {
	
	private String map_data_1;
	private String map_data_2;
	
	public MapDTO() {
		super();
	}

	public MapDTO(String map_data_1, String map_data_2) {
		super();
		this.map_data_1 = map_data_1;
		this.map_data_2 = map_data_2;
	}

	public String getMap_data_1() {
		return map_data_1;
	}

	public void setMap_data_1(String map_data_1) {
		this.map_data_1 = map_data_1;
	}

	public String getMap_data_2() {
		return map_data_2;
	}

	public void setMap_data_2(String map_data_2) {
		this.map_data_2 = map_data_2;
	}

		
}
